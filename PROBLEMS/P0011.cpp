// write a c program  check given numbers positive or negative


#include<iostream>
using namespace std;
int main()
{
   int num;
   cout<<"enter the num:";
   cin>>num;
   if(num>0)
   {
      cout<<"1"<<endl;
   }
   else if(num>=0)
   {
      cout<<"0";
   }
   else
   {
      cout<<"-1";
   }
   


}