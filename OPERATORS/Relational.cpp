#include<iostream>
using namespace std;
int main()
{
    int a,b;
    cout<<"enter a value:";
    cin>>a;
    cout<<"enter b value:";
    cin>>b;
    cout<<"IS a is equals to b:"<<(a==b)<<endl;
    cout<<"IS a is not equals to b:"<<(a!=b)<<endl;
    cout<<"IS a is less than or equals to b:"<<(a<=b)<<endl;
    cout<<"IS a is greater than or equals to b:"<<(a>=b)<<endl;
    cout<<"IS a is less than b:"<<(a<b)<<endl;
    cout<<"IS a is greater than b:"<<(a>b)<<endl;
    return 0;
}